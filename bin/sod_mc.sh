#!/bin/bash
# sod_mc.sh — Monte Carlo sampling of the configuration space with SOD's PME Hamiltonian.
#
# This script calls mcsod, which uses the Periodic Motif Expansion (PME) effective
# Hamiltonian to estimate energies during an MC walk and outputs mode-dependent
# ENSEMBLE rows at the target substitution level.  It is intended for
# target levels where the exact enumeration (sod_comb.sh → sod_pme.sh) becomes
# intractable.
#
# USAGE
#   sod_mc.sh [-model <filename>]
#
#   -model <filename>   Use a named model file instead of the default pme.model.
#                       Example: sod_mc.sh -model pme_12.model
#
#   Run from the main problem directory, i.e. the directory that contains:
#     INMC        — MC input file (sampler, symmetry mode, n_prod, start, trace, ...)
#     INSOD       — SOD input file (defines the target level via the nsubs line)
#     SGO         — Space group operators file
#     EQMATRIX    — Equivalence matrix (generated by sod_comb.sh)
#     TEMPERATURES — temperature list for Metropolis sampling (one K value per line)
#     pme.model   — PME calibration/control file (or the file specified with -model)
#     n00/ENERGIES        — Reference energy for 0 substitutions
#     n01/ENSEMBLE, n01/ENERGIES — Level 1 reference configurations and energies
#     n02/ENSEMBLE, n02/ENERGIES — Level 2 reference configurations and energies
#     (n03/, n04/ — optional; higher orders improve accuracy)
#
# OUTPUT
#     nXX/PMEx/MCT_TTTK/ENSEMBLE, ENERGIES, OUTMC, MCTRACE (Metropolis)
#     nXX/PMEx/MCU/ENSEMBLE, ENERGIES, OUTMC                  (uniform random)
#
# Recalibration is controlled by pme.model and nXX/ENERGIES.

# Resolve script directory and find executables
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
export PATH="${SCRIPT_DIR}:${PATH}"

clear

# Separate -model <filename> from other args
MODEL_ARGS=()
while [[ $# -gt 0 ]]; do
  case "$1" in
    -model)
      MODEL_ARGS+=("-model" "$2")
      shift 2
      ;;
    *)
      echo "Error: unexpected argument to sod_mc.sh: $1"
      exit 1
      ;;
  esac
done

# Check that required input files are present before launching
for reqfile in INMC INSOD SGO EQMATRIX; do
  if [ ! -f "$reqfile" ]; then
    echo "Error: required file '$reqfile' not found in the current directory."
    echo "Run sod_mc.sh from the main problem directory (where INMC, INSOD, SGO, EQMATRIX are)."
    exit 1
  fi
done

if [ ! -f "n00/ENERGIES" ] || [ ! -f "n01/ENSEMBLE" ] || [ ! -f "n01/ENERGIES" ] || \
   [ ! -f "n02/ENSEMBLE" ] || [ ! -f "n02/ENERGIES" ]; then
  echo "Error: n00/ENERGIES, n01/ENSEMBLE+ENERGIES and n02/ENSEMBLE+ENERGIES are required."
  echo "These are the minimum training data for the PME effective Hamiltonian."
  exit 1
fi

SAMPLER=$(awk 'NF && $1 !~ /^#/ {print $1; exit}' INMC)

if [ "$SAMPLER" = "1" ]; then
  if [ ! -f TEMPERATURES ]; then
    echo "Error: TEMPERATURES not found for Metropolis sampling."
    exit 1
  fi
  while read -r temp rest; do
    case "$temp" in
      ""|\#*) continue ;;
    esac
    mcsod "${MODEL_ARGS[@]}" "$temp" || exit 1
  done < TEMPERATURES
elif [ "$SAMPLER" = "2" ]; then
  mcsod "${MODEL_ARGS[@]}" || exit 1
else
  echo "Error: invalid sampler in INMC (1=Metropolis, 2=Uniform): $SAMPLER"
  exit 1
fi

exit 0
