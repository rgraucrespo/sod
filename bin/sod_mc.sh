#!/bin/bash
# sod_mc.sh — Metropolis Monte Carlo sampling with SOD's PME Hamiltonian.
#
# This script calls mcsod, which uses the Periodic Motif Expansion (PME) effective
# Hamiltonian to estimate energies during a Metropolis MC walk and outputs ENSEMBLE
# rows at the target substitution level.  It is intended for target levels where
# the exact enumeration (sod_comb.sh → sod_pme.sh) becomes intractable.
# For energy-free uniform random sampling, use sod_random.sh instead.
#
# USAGE
#   sod_mc.sh [-model <filename>]
#
#   -model <filename>   Use a named model file instead of the default pme.model.
#                       Example: sod_mc.sh -model pme_12.model
#
#   Run from the SODPROJECT directory, i.e. the directory that contains:
#     INMC        — MC input file (symmetry mode, n_prod, start, trace, equil, ...)
#     INSOD       — SOD input file (defines the target level via the nsubs line)
#     SGO         — Space group operators file
#     EQMATRIX    — Equivalence matrix (generated by sod_comb.sh)
#     TEMPERATURES — temperature list (one K value per line)
#     pme.model   — PME calibration/control file (or the file specified with -model)
#     level 0 ENERGIES        — Reference energy for 0 substitutions
#     level 1 ENSEMBLE+ENERGIES — Reference configurations and energies
#     level 2 ENSEMBLE+ENERGIES — Reference configurations and energies
#     (levels 3 and 4 are optional; higher orders improve accuracy)
#     Level directories may be n01/n02 or wider, e.g. n001/n002.
#
# OUTPUT
#     nXX/MCT_TTTK/PMEx/{ENSEMBLE, ENERGIES, OUTMC, MCTRACE}
#
# Recalibration is controlled by pme.model and nXX/ENERGIES.

# Resolve script directory and find executables
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
export PATH="${SCRIPT_DIR}:${PATH}"
. "${SCRIPT_DIR}/sod_common.sh"

SODPROJECT="$(sod_require_project_root "$PWD")" || exit 1
cd "$SODPROJECT" || exit 1

clear

# Separate -model <filename> from other args
MODEL_ARGS=()
while [[ $# -gt 0 ]]; do
  case "$1" in
    -model)
      MODEL_ARGS+=("-model" "$2")
      shift 2
      ;;
    *)
      echo "Error: unexpected argument to sod_mc.sh: $1"
      exit 1
      ;;
  esac
done

# Check that required input files are present before launching
for reqfile in INMC INSOD SGO EQMATRIX; do
  if [ ! -f "$reqfile" ]; then
    echo "Error: required file '$reqfile' not found in SODPROJECT/."
    exit 1
  fi
done

# Metropolis sampling drives the walk with the PME effective Hamiltonian, so it
# requires the reference training data and a list of temperatures.
# For energy-free uniform random sampling, use sod_random.sh instead.
n0dir=$(sod_level_dir_by_number "$SODPROJECT" 0)
n1dir=$(sod_level_dir_by_number "$SODPROJECT" 1)
n2dir=$(sod_level_dir_by_number "$SODPROJECT" 2)
if [ -z "$n0dir" ] || [ -z "$n1dir" ] || [ -z "$n2dir" ] || \
   [ ! -f "$n0dir/ENERGIES" ] || [ ! -f "$n1dir/ENSEMBLE" ] || [ ! -f "$n1dir/ENERGIES" ] || \
   [ ! -f "$n2dir/ENSEMBLE" ] || [ ! -f "$n2dir/ENERGIES" ]; then
  echo "Error: level 0 ENERGIES, level 1 ENSEMBLE+ENERGIES and level 2 ENSEMBLE+ENERGIES are required."
  echo "These are the minimum training data for the PME effective Hamiltonian."
  exit 1
fi
if [ ! -f TEMPERATURES ]; then
  echo "Error: TEMPERATURES not found for Metropolis sampling."
  exit 1
fi
while read -r temp rest; do
  case "$temp" in
    ""|\#*) continue ;;
  esac
  mcsod "${MODEL_ARGS[@]}" "$temp" || exit 1
done < TEMPERATURES

exit 0
