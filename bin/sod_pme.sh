#!/bin/bash
# sod_pme.sh — Run the Periodic Motif Expansion (PME) effective Hamiltonian in SOD.
#
# This script calls pmesod, which fits a motif expansion Hamiltonian (up to 4-body)
# from the reference energies in n00..n04 (low-side) and n(M)..n(M-4) (high-side,
# optional), and predicts energies for all configurations at the target substitution
# level.
#
# USAGE
#   sod_pme.sh [-model <filename>]
#
#   -model <filename>   Use a named model file instead of the default pme.model.
#                       Example: sod_pme.sh -model pme_12.model
#
#   Run from the main problem directory, i.e. the directory that contains:
#     INSOD       — SOD input file (defines the target level via the nsubs line)
#     pme.model   — Optional PME control/calibration file (see 2-phase workflow below)
#                   (or the file specified with -model)
#     SGO         — Space group operators file
#     EQMATRIX    — Equivalence matrix (generated by sod_comb.sh)
#     n00/ENERGIES        — Reference energy for 0 substitutions
#     n01/ENSEMBLE, n01/ENERGIES — Level 1 reference configurations and energies
#     n02/ENSEMBLE, n02/ENERGIES — Level 2 reference configurations and energies
#     (n03/, n04/ — optional; higher orders improve accuracy)
#
# OUTPUT (written to the target level directory nXX/)
#     nXX/PME0/ENERGIES — low-side predictions
#     nXX/PME1/ENERGIES — high-side predictions, when available
#     nXX/PMEh/ENERGIES — hybrid predictions, when available
#     pme.model.tmp     — suggested control file (written when no pme.model present)
#     nXX/pme.model     — copy of pme.model used for this target
#
# 2-PHASE PME WORKFLOW
#
#   Phase 1 — Referencing: fit motif interaction terms (V) from boundary concentrations.
#
#     1a. Run DFT for the low-side reference levels n00..n04 and, for PMEh, the
#         symmetric high-side levels n(M)..n(M-4).  Place energies in nXX/ENERGIES.
#
#     1b. Run sod_pme.sh with no pme.model present.
#         → Fits V terms; predicts energies for all target-level configurations;
#           writes pme.model.tmp with a bisection-selected calib_config_list
#           (up to 9 configs spanning the energy range) and default eps/alpha/eta.
#
#   Phase 2 — Calibrating (optional): fit epsilon correction terms (ε) from
#   a small set of DFT calculations at the target concentration.
#
#     2a. Run sod_gener.sh -choose <indices> for the config indices in calib_config_list.
#         Compute DFT energies for those configurations and add them to nXX/ENERGIES.
#
#     2b. Rename pme.model.tmp to pme.model (edit n_calib if needed), re-run sod_pme.sh.
#         → Fits epsilon values; pme.model.tmp is updated with calibrated eps (n_calib=0)
#           for use on subsequent runs without re-fitting.
#
# For Monte Carlo sampling of the configuration space with the PME Hamiltonian,
# use sod_mc.sh (runs mcsod, reads INMC).

# Resolve script directory and find executables
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
export PATH="${SCRIPT_DIR}:${PATH}"

clear

# Check that required input files are present before launching
for reqfile in INSOD SGO EQMATRIX; do
  if [ ! -f "$reqfile" ]; then
    echo "Error: required file '$reqfile' not found in the current directory."
    echo "Run sod_pme.sh from the main problem directory (where INSOD, SGO, EQMATRIX are)."
    exit 1
  fi
done

if [ ! -f "n00/ENERGIES" ] || [ ! -f "n01/ENSEMBLE" ] || [ ! -f "n01/ENERGIES" ] || \
   [ ! -f "n02/ENSEMBLE" ] || [ ! -f "n02/ENERGIES" ]; then
  echo "Error: n00/ENERGIES, n01/ENSEMBLE+ENERGIES and n02/ENSEMBLE+ENERGIES are required."
  echo "These are the minimum training data for the effective Hamiltonian."
  exit 1
fi

# Pass all arguments through to pmesod (-model <filename> is forwarded as-is)
pmesod "$@"
exit 0
